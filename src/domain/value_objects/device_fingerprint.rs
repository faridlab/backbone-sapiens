//! Device fingerprint value object
//!
//! Provides device identification and fingerprinting capabilities.

use serde::{Deserialize, Serialize};
use std::fmt;
use uuid::Uuid;

/// Device fingerprint value object
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct DeviceFingerprint(String);

impl DeviceFingerprint {
    /// Create a new device fingerprint
    pub fn new(fingerprint: &str) -> Self {
        DeviceFingerprint(fingerprint.to_string())
    }

    /// Generate a new unique device fingerprint
    pub fn generate() -> Self {
        DeviceFingerprint(format!("fp_{}", Uuid::new_v4()))
    }

    /// Get the fingerprint as a string
    pub fn as_str(&self) -> &str {
        &self.0
    }

    /// Get the fingerprint as a string reference
    pub fn as_string(&self) -> String {
        self.0.clone()
    }

    /// Get the fingerprint value (alias for as_str)
    pub fn value(&self) -> &str {
        self.as_str()
    }

    /// Consume and return the inner string
    pub fn into_string(self) -> String {
        self.0
    }

    /// Create from string
    pub fn from(s: String) -> Self {
        DeviceFingerprint(s)
    }

    /// Validate if the fingerprint format looks valid
    pub fn is_valid(&self) -> bool {
        // Basic validation: not empty and reasonable length
        !self.0.is_empty() && self.0.len() <= 255
    }

    /// Get the fingerprint type (generated vs provided)
    pub fn fingerprint_type(&self) -> FingerprintType {
        if self.0.starts_with("fp_") {
            FingerprintType::Generated
        } else {
            FingerprintType::Provided
        }
    }

    // <<< CUSTOM CODE START >>>
    // Additional methods for authentication

    /// Generate a random token hash (for session token_hash field)
    pub fn generate_token_hash() -> String {
        use sha2::{Sha256, Digest};
        let random_bytes = Uuid::new_v4().as_bytes().to_vec();
        let mut hasher = Sha256::new();
        hasher.update(&random_bytes);
        format!("{:x}", hasher.finalize())
    }
    // <<< CUSTOM CODE END >>>
}

impl fmt::Display for DeviceFingerprint {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.0)
    }
}

impl From<String> for DeviceFingerprint {
    fn from(s: String) -> Self {
        DeviceFingerprint(s)
    }
}

impl From<&str> for DeviceFingerprint {
    fn from(s: &str) -> Self {
        DeviceFingerprint(s.to_string())
    }
}

/// Types of device fingerprints
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum FingerprintType {
    /// Generated by the system
    Generated,
    /// Provided by client
    Provided,
}

/// Validation error type for device fingerprint
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ValidationError(pub &'static str);

impl fmt::Display for ValidationError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{}", self.0)
    }
}

impl std::error::Error for ValidationError {}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_device_fingerprint_creation() {
        let fp1 = DeviceFingerprint::new("custom_fp_123");
        assert_eq!(fp1.as_str(), "custom_fp_123");
        assert_eq!(fp1.fingerprint_type(), FingerprintType::Provided);

        let fp2 = DeviceFingerprint::generate();
        assert!(fp2.as_str().starts_with("fp_"));
        assert_eq!(fp2.fingerprint_type(), FingerprintType::Generated);
        assert!(fp2.is_valid());
    }

    #[test]
    fn test_device_fingerprint_validation() {
        let valid_fp = DeviceFingerprint::new("valid_fingerprint");
        assert!(valid_fp.is_valid());

        let empty_fp = DeviceFingerprint::new("");
        assert!(!empty_fp.is_valid());

        let long_fp = DeviceFingerprint::new("x".repeat(300).as_str());
        assert!(!long_fp.is_valid());
    }

    #[test]
    fn test_device_fingerprint_conversions() {
        let fp1: DeviceFingerprint = "test_fp".into();
        assert_eq!(fp1.as_str(), "test_fp");

        let fp2: DeviceFingerprint = "test_fp".to_string().into();
        assert_eq!(fp2.as_str(), "test_fp");

        assert_eq!(fp1, fp2);
    }

    #[test]
    fn test_device_fingerprint_uniqueness() {
        let fp1 = DeviceFingerprint::generate();
        let fp2 = DeviceFingerprint::generate();
        assert_ne!(fp1, fp2);
    }
}