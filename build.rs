//! Build script for Sapiens module
//!
//! This script compiles Protocol Buffer definitions to Rust code using tonic-build.
//! The generated code is placed in `src/generated/` directory.
//!
//! ## Why tonic-build instead of buf generate?
//!
//! - `tonic-build`: Uses absolute `crate::` paths - BEST PRACTICE
//! - `buf generate`: Uses relative `super::super::` paths - harder to maintain
//!
//! ## Proto Structure
//!
//! The proto files are organized following DDD patterns:
//! - `proto/domain/entity/` - Domain entities (User, Role, Permission, etc.)
//! - `proto/domain/repository/` - Repository service definitions
//! - `proto/domain/usecase/` - Use case commands and queries (CQRS)
//!
//! ## Usage
//!
//! Proto compilation is enabled by default. To disable:
//! Set SAPIENS_DISABLE_PROTOS=1 environment variable

fn main() -> Result<(), Box<dyn std::error::Error>> {
    // Proto compilation is DISABLED by default because:
    // 1. Proto files use buf/validate which requires buf CLI setup
    // 2. The module uses Rust-native domain entities instead
    //
    // To enable proto compilation:
    // 1. Install buf CLI and run `buf mod update` in proto/ directory
    // 2. Set SAPIENS_COMPILE_PROTOS=1 environment variable
    //
    // Current approach: Domain entities are defined in Rust (src/domain/entity/)
    // which provides better type safety and IDE support.
    if std::env::var("SAPIENS_COMPILE_PROTOS").is_ok() {
        compile_protos()?;
    } else {
        println!("cargo:warning=Proto compilation disabled. Using Rust domain entities.");
    }

    Ok(())
}

fn compile_protos() -> Result<(), Box<dyn std::error::Error>> {
    use std::path::PathBuf;

    // Only regenerate if proto files changed
    println!("cargo:rerun-if-changed=proto/");

    // Get the output directory
    let out_dir = PathBuf::from("src/generated");

    // Ensure output directory exists
    std::fs::create_dir_all(&out_dir)?;

    // Collect all proto files from domain layer
    let proto_files = collect_proto_files("proto/domain")?;

    if proto_files.is_empty() {
        println!("cargo:warning=No proto files found in proto/domain/");
        return Ok(());
    }

    println!("cargo:warning=Compiling {} proto files...", proto_files.len());

    // Configure tonic-build for better code generation
    // Using absolute crate paths instead of relative super:: paths
    tonic_build::configure()
        .build_server(true)
        .build_client(true)
        .build_transport(true)
        .out_dir(&out_dir)
        // Use crate-level paths for better maintainability
        .extern_path(".google.protobuf", "::prost_types")
        // Add serde derives for JSON serialization
        .type_attribute(".", "#[derive(serde::Serialize, serde::Deserialize)]")
        // Make optional fields use Option with default
        .field_attribute(".", "#[serde(default)]")
        // Compile proto files
        // The include path "." allows imports like "proto/domain/entity/..." to work
        .compile_protos(
            &proto_files,
            &[
                ".",  // Root of sapiens module - allows "proto/domain/..." imports
            ],
        )?;

    // Generate the mod.rs file that includes all generated code
    generate_mod_file(&out_dir, &proto_files)?;

    println!("cargo:warning=Proto compilation completed successfully");

    Ok(())
}

/// Generate a mod.rs file that includes all generated proto files
fn generate_mod_file(
    out_dir: &std::path::Path,
    _proto_files: &[String],
) -> Result<(), Box<dyn std::error::Error>> {
    use std::io::Write;

    let mod_file = out_dir.join("mod.rs");
    let mut file = std::fs::File::create(&mod_file)?;

    writeln!(file, "//! Generated Rust code from Protocol Buffer definitions")?;
    writeln!(file, "//!")?;
    writeln!(file, "//! This file is auto-generated by build.rs using tonic-build.")?;
    writeln!(file, "//! Do not edit manually - changes will be overwritten.")?;
    writeln!(file, "//!")?;
    writeln!(file, "//! To regenerate: cargo build")?;
    writeln!(file)?;
    writeln!(file, "#![allow(dead_code)]")?;
    writeln!(file, "#![allow(unused_imports)]")?;
    writeln!(file, "#![allow(clippy::all)]")?;
    writeln!(file, "#![allow(clippy::derive_partial_eq_without_eq)]")?;
    writeln!(file)?;

    // Include the generated files using tonic-build naming convention
    // tonic-build generates files like: sapiens.domain.entity.rs
    writeln!(file, "// Domain Entity types")?;
    writeln!(file, "pub mod entity {{")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.entity\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    writeln!(file, "// Domain Event types")?;
    writeln!(file, "pub mod event {{")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.event\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    writeln!(file, "// Domain Repository types")?;
    writeln!(file, "pub mod repository {{")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.repository\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    writeln!(file, "// Domain Service types")?;
    writeln!(file, "pub mod service {{")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.service\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    writeln!(file, "// Domain Specification types")?;
    writeln!(file, "pub mod specification {{")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.specification\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    writeln!(file, "// Domain Value Object types")?;
    writeln!(file, "pub mod value_object {{")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.value_object\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    writeln!(file, "// Use Case types")?;
    writeln!(file, "pub mod usecase {{")?;
    writeln!(file, "    // Base usecase")?;
    writeln!(file, "    tonic::include_proto!(\"sapiens.domain.usecase\");")?;
    writeln!(file, "}}")?;
    writeln!(file)?;

    // Re-exports for convenience
    writeln!(file, "/// Convenience re-exports")?;
    writeln!(file, "pub mod prelude {{")?;
    writeln!(file, "    pub use super::entity::*;")?;
    writeln!(file, "    pub use super::value_object::*;")?;
    writeln!(file, "}}")?;

    Ok(())
}

/// Recursively collect all .proto files from a directory
fn collect_proto_files(dir: &str) -> Result<Vec<String>, Box<dyn std::error::Error>> {
    use std::path::PathBuf;

    let mut proto_files = Vec::new();
    let path = PathBuf::from(dir);

    if !path.exists() {
        println!("cargo:warning=Proto directory does not exist: {}", dir);
        return Ok(proto_files);
    }

    collect_proto_files_recursive(&path, &mut proto_files)?;

    Ok(proto_files)
}

fn collect_proto_files_recursive(
    dir: &std::path::Path,
    files: &mut Vec<String>,
) -> Result<(), Box<dyn std::error::Error>> {
    if dir.is_dir() {
        for entry in std::fs::read_dir(dir)? {
            let entry = entry?;
            let path = entry.path();

            if path.is_dir() {
                collect_proto_files_recursive(&path, files)?;
            } else if let Some(ext) = path.extension() {
                if ext == "proto" {
                    if let Some(path_str) = path.to_str() {
                        files.push(path_str.to_string());
                        println!("cargo:rerun-if-changed={}", path_str);
                    }
                }
            }
        }
    }

    Ok(())
}
